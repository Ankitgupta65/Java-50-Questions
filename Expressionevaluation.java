public class Expressionevaluation {
    public static void main(String[] args) {
        double a = 25.5; // First number
        double b = 3.5; // Second number
        double c = 40.5; // Third number
        double d = 4.5; // Fourth number
        double result = (a*b-b*b) / (c-d); // Calculate the expression

        System.out.println("The result of the expression is: " + result); // Print the result
    }
}
