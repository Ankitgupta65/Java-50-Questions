public class Arithmaticoperations {
    public static void main(String[] args) {
        double a = 25.5; // First number
        double b = 10; // Second number

        double sum = a + b; // Calculate the sum
        double difference = a - b; // Calculate the difference
        double product = a * b; // Calculate the product
        double division = a / b; // Calculate the division

        System.out.println("The sum of " + a + " and " + b + " is: " + sum); // Print the sum
        System.out.println("The difference of " + a + " and " + b + " is: " + difference); // Print the difference
        System.out.println("The product of " + a + " and " + b + " is: " + product); // Print the product
        System.out.println("The division of " + a + " by " + b + " is: " + division); // Print the division
    }
}
